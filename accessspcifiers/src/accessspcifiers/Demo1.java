package accessspcifiers;

public class Demo1 {
	public static void main(String[] args) {
		// float type
		float f = 20.25f;
		System.out.println("f="+f);
		
		// double type
		double d = 20.25;
		System.out.println("d=" + d);
		
		char ch='S';
		System.out.println(ch);
		
		char ch1='&';
		System.out.println(ch1);
		
		char ch2='$';
		System.out.println(ch2);
	}

}

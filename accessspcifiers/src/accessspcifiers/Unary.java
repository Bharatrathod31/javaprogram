package accessspcifiers;

public class Unary {
	public static void main(String[] args) {
		// integer declared
		int a =10;
		int b =10;
		
		System.out.println("Postincrement: "+(a++));
		System.out.println("Preinecrement: "+(++a));
		
		System.out.println("Postdecrement: "+ (b--));
		System.out.println("Predecrement: " + (--b));
	}

}
